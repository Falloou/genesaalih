                    <h2 class="mb-4">Mot du Président</h2>
                    <p>Aujourd’hui plus que jamais, l’Etat du Sénégal a adopté comme référentiels de développement économique, social et environnemental, le Plan Sénégal Emergent (PSE) qui repose sur trois axes: Axe 1 qui vise la transformation structurelle de l’économie, Axe 2 le développement du capital humain,et Axe 3 la bonne gouvernance ; mais aussi, l’Acte 3 de la décentralisation dont la vision est d’organiser le Sénégal en territoires viables et compétitifs porteurs de développement durable.

                    En effet, malgré les efforts consentis par l’Etat du Sénégal,  les problèmes de la jeunesse restent multiples. Ils se manifestent dans un contexte marqué par la dégradation du potentiel productif, l’augmentation des besoins liées à la croissance démographique, la persistance de la pauvreté et l’insécurité alimentaire, le manque d’emplois, la non scolarisation et le manque de formation, la discrimination à l’égard des femmes et des couches vulnérables tels que les personnes en situation d’handicap, les talibés ou enfants de la rue qui constitue une problématique pour autorités. En effet, chaque année le Sénégal enregistre plusieurs milliers de jeunes qui après la mémorisation du Coran, se trouvent confrontés à un problème de qualification qui leur permettraient d’intégrer le monde professionnel.  Ces différents éléments contextuels ont contribué à l’aggravation de la situation dans laquelle vivent les jeunes vulnérables tant en milieu rural qu’en milieu urbain. 

                    Face à cette situation, à travers une approche territoriale, l’association  GENERATION SAALIH (GS) est née pour contribuer à la mise en œuvre des politiques publiques de l’Etat en proposant des programmes d’accompagnement à l’insertion socioprofessionnelle. Elle s’inspire du modèle de GESTION des Daaras de SERIGNE SALIOU MBACKE à travers  KHELCOM où l'enseignement du Coran et l'éducation religieuse étaient associés au travail (l'apprentissage d'un métier) pour indiquer qu'il s'agissait d'activités inséparables pour rendre le talibé et le dongo tarbiaya autonome et responsable. </p>
